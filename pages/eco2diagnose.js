import Head from 'next/head';

import Modal from '../components/modal/modal';
import { getUser } from '../util/api/user';
import { Eco2DiagnoseAI } from '../components/eco2diagnose/Eco2DiagnoseAI';

export default function eco2diagnose(props) {
  const iasAccessArray = props?.user?.plan?.ias_access
    ? props.user.plan.ias_access.split(',').map((item) => item.trim())
    : [];

  const isEco2DescAvailable = iasAccessArray.includes('Eco2Desc');
  return (
    <div>
      <Head>
        <title>ECO2DIAGNOSE</title>
        <meta name="diagnose" content="Generated by ECOÂ² Colombia" />
        <link rel="icon" href="/iconografia/eco2it_logo.jpeg" />
      </Head>
      {/* If it is not a plan, or the plan does not have access to chatGpt, or the user has no tokens, show the modal */}
      {!props.user.plan ? (
        <Modal user={props?.user} />
      ) : !isEco2DescAvailable ? (
        <Modal user={props?.user} />
      ) : +props?.user?.plan?.max_tokens <= 0 ? (
        <Modal user={props?.user} />
      ) : (
        <Eco2DiagnoseAI user={props.user.id} />
      )}
    </div>
  );
}
export const getServerSideProps = async (context) => {
  const result = await getUser(context);

  if (!result?.data) {
    return {
      redirect: {
        permanent: false,
        destination: '/'
      }
    };
  }

  return {
    props: {
      user: result?.data,
      session: result?.session
    }
  };
};
